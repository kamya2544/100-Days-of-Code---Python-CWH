# Type casting or Type Conversion
a="1"
b="2"
print (a+b)
print (int(a+b))
print (int(a)+int(b))
c=1
d=2
print(c+d)

'''Types of Conversion:
Explicit: via developer
Implicit: via Python (lower converted into upper itself)
'''
e=1.9
f=8
print(e+f) #implicitly int converted to float datatype itself